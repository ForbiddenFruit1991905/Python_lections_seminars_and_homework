# ✔ Сформируйте список с порядковыми номерами нечётных элементов исходного списка.
# ✔ Нумерация начинается с единицы.

my_list = [1, 9, 3, 3, 5, 6, 5, 7, 9, 3]
unique_list = []

for i, elem in enumerate(my_list, start=1):
    if elem % 2 != 0:
        unique_list.append(i)
print(unique_list)