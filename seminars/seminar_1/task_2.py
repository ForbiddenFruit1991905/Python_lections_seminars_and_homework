# 📌 Посчитайте сумму чётных элементов от 1 до n исключая кратные e.
# 📌 Используйте while и if.
# 📌 Попробуйте разные значения e и n.

a = 0
n = 10
e = 3
count = 0

while a < n:
    a += 1
    if a % 2 == 0 and a % e != 0:
        count += a
print(count)