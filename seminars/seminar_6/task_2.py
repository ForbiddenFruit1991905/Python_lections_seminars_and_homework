from random import randint

def func_guess_num(LOW_BOUND: int, UPPER_BOUND: int, ATTEMPTS: int):
    random_num = randint(LOW_BOUND, UPPER_BOUND)
    z = (f'–í –¥–∏–∞–ø–∞–∑–æ–Ω–µ –æ—Ç {LOW_BOUND} –¥–æ {UPPER_BOUND} '
         f'–ø–æ–ª—É—á–∏–ª–∏ {random_num}')
    print(z)
    attempt = 0
    while attempt < ATTEMPTS:
        person_attempt = int(input(f"–£–≥–∞–¥–∞–π—Ç–µ —á–∏—Å–ª–æ –≤ —É–∫–∞–∑–∞–Ω–Ω–æ–º –¥–∏–∞–ø–∞–∑–æ–Ω–µ –æ—Ç {LOW_BOUND} –¥–æ {UPPER_BOUND}: "))
        if LOW_BOUND <= person_attempt <= UPPER_BOUND:
            if person_attempt < random_num:
                print("–í—ã –Ω–µ —É–≥–∞–¥–∞–ª–∏. –ü–æ–ø—Ä–æ–±—É–π—Ç–µ –µ—â–µ —Ä–∞–∑. –ß–∏—Å–ª–æ –¥–æ–ª–∂–Ω–æ –±—ã—Ç—å –±–æ–ª—å—à–µ —É–∫–∞–∑–∞–Ω–Ω–æ–≥–æ.")
            elif person_attempt > random_num:
                print("–í—ã –Ω–µ —É–≥–∞–¥–∞–ª–∏. –ü–æ–ø—Ä–æ–±—É–π—Ç–µ –µ—â–µ —Ä–∞–∑. –ß–∏—Å–ª–æ –¥–æ–ª–∂–Ω–æ –±—ã—Ç—å –º–µ–Ω—å—à–µ —É–∫–∞–∑–∞–Ω–Ω–æ–≥–æ.")
            else:
                return True
            attempt += 1
        else:
            print("–í—ã –≤–≤–µ–ª–∏ –∑–Ω–∞—á–µ–Ω–∏–µ –Ω–µ –∏–∑ —É–∫–∞–∑–∞–Ω–Ω–æ–≥–æ –¥–∏–∞–ø–∞–∑–æ–Ω–∞")
    return False

