# Оператор is
# Ещё один способ взаимодействия с типами данных — оператор is.
# Он похож на сравнение (двойное равно), но сравнивает не значения, а сами объекты.
# По сути оператор is занимается сравнением "айдишников" объектов, т.е. проверяет
# лежат ли объекты в одном месте в памяти компьютера.

num = 2 + 2 * 2
digit = 36 / 6
print(num == digit)
print(num is digit)