# Строгая типизация
# Каждый раз возвращается новый адрес объекта в памяти.
# Python не меняет класс объекта.
# Он меняет переменную. Каждый раз она ссылается на новый объект.
# Такая механика прописана в самом интерпретаторе.

a = 5
print(type(a), id(a))
a = "hello world"
print(type(a), id(a))
a = 42.0 * 3.141592 / 2.71828
print(type(a), id(a))